lista = [124, 345, 5, 72, 46, 6, 7, 3, 1, 7, 0]

# função para ordenar lista
# altera a lista original
lista.sort()
print(lista)

# método sorted ordena, mas requer variável
lista = sorted(lista)
print(lista)

# _______________________________________

# ordenar por decrescentes
lista.sort(reverse=True)
print(lista)

# inverter a ordem da lista
lista.reverse()
print(lista)

